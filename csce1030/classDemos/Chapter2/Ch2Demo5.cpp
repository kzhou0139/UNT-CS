#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	double number=18.0;
	
	double result=sqrt(number);
	cout<<"result="<<result<<endl;
	
	cout<<"square root of 26.0 is "<<sqrt(26.0)<<endl; 
	
	
	result=pow(2.0,3.0); //2.0 ^ 3.0
	cout<<"result="<<result<<endl;
	
	cout<<"another power:"<<pow(15, 1.0/3.0)<<endl;
	
	
	int x=-9;
	cout<<"Absolute value="<<abs(x)<<endl;
		
	return 0;
}






